func TestToPin_UnknownKeysGoToEnv(t *testing.T) {
	pin, notes := ToPin([]KV{
		{"OPENAI_API_KEY", "sk-test"},
		{"MY_CUSTOM_VAR", "x"},
	})
	if pin.Env["OPENAI_API_KEY"] != "sk-test" {
		t.Errorf("OPENAI_API_KEY should be preserved in [env], got %v", pin.Env)
	}
	if pin.Env["MY_CUSTOM_VAR"] != "x" {
		t.Errorf("MY_CUSTOM_VAR should be preserved in [env], got %v", pin.Env)
	}
	// Notes should mention "preserved verbatim".
	joined := strings.Join(notes, "\n")
	if !strings.Contains(joined, "preserved verbatim") {
		t.Errorf("expected verbatim-preservation note, got %s", joined)
	}
}

func TestToPin_UsernameIsDropped(t *testing.T) {
	pin, notes := ToPin([]KV{{"USERNAME", "old-user"}})
	// USERNAME has no dedicated pin field anymore — it's a build flag.
	if pin.Env["USERNAME"] != "" {
		t.Errorf("USERNAME should not be preserved under [env], got %v", pin.Env)
	}
	joined := strings.Join(notes, "\n")
	if !strings.Contains(joined, "ignored") {
		t.Errorf("expected ignored note for USERNAME, got %s", joined)
	}
}

