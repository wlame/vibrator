func TestParseDotenv_BasicLines(t *testing.T) {
	got, err := ParseDotenv(strings.NewReader(`# comment
HARNESS=claude-code
PROFILE=full
EMPTY=
`))
	if err != nil {
		t.Fatalf("ParseDotenv: %v", err)
	}
	want := []KV{
		{"HARNESS", "claude-code"},
		{"PROFILE", "full"},
		{"EMPTY", ""},
	}
	if !reflect.DeepEqual(got, want) {
		t.Errorf("got %v, want %v", got, want)
	}
}

func TestParseDotenv_StripsExport(t *testing.T) {
	got, _ := ParseDotenv(strings.NewReader(`export HARNESS=claude-code`))
	if len(got) != 1 || got[0].Key != "HARNESS" {
		t.Errorf("export prefix not stripped: %v", got)
	}
}

func TestParseDotenv_UnquotesMatchingQuotes(t *testing.T) {
	cases := map[string]string{
		`X="hello world"`: "hello world",
		`X='it'`:          "it",
		`X=plain`:         "plain",
		`X="mismatched'`:  `"mismatched'`, // unchanged — quotes don't match
		`X=""`:            "",
	}
	for in, wantVal := range cases {
		got, _ := ParseDotenv(strings.NewReader(in))
		if len(got) != 1 {
			t.Errorf("input %q produced %d entries", in, len(got))
			continue
		}
		if got[0].Value != wantVal {
			t.Errorf("input %q → value %q, want %q", in, got[0].Value, wantVal)
		}
	}
}

