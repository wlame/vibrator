func TestRealisticVbEnvFile(t *testing.T) {
	// Mirror the structure of the user's actual .vb.env (sanitized).
	input := `# vibrator project pin — generated by interactive menu
# Edit by hand or re-run vibrate.
PROFILE=full
NO=aider

# claude-mem (auto-bootstrapped ... by vibrator)
CLAUDE_MEM_SERVER_BETA_API_KEY=cmem_864edc435eec72860a51c6fae5c3d999c6f3eceeb0ac8578
CLAUDE_MEM_SERVER_BETA_TEAM_ID=63d75e4d-835a-4843-95c4-2580a5678ecf
CLAUDE_MEM_SERVER_BETA_PROJECT_ID=469bf5e5-e852-499e-bdc0-66eb5ccfd910
`
	kvs, err := ParseDotenv(strings.NewReader(input))
	if err != nil {
		t.Fatalf("ParseDotenv: %v", err)
	}
	pin, _ := ToPin(kvs)

	if pin.Profile != "full" {
		t.Errorf("Profile = %q", pin.Profile)
	}
	if !reflect.DeepEqual(pin.No, []string{"aider"}) {
		t.Errorf("No = %v", pin.No)
	}
	cm := pin.Prereqs["claude-mem-server-beta"]
	if cm == nil || cm["api_key"] == "" || cm["team_id"] == "" || cm["project_id"] == "" {
		t.Errorf("claude-mem prereq incomplete: %+v", cm)
	}
}
