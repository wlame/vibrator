func TestToPin_MapsCoreFields(t *testing.T) {
	kvs := []KV{
		{"HARNESS", "claude-code"},
		{"PROFILE", "full"},
		{"SHELL", "zsh"},
		{"WITH", "playwright,audit-toolkit"},
		{"NO", "aider"},
		{"CATALOG", "claude-mem,context7"},
	}
	pin, notes := ToPin(kvs)

	if pin.Harness != "claude-code" || pin.Profile != "full" || pin.Shell != "zsh" {
		t.Errorf("core scalars wrong: %+v", pin)
	}
	if !reflect.DeepEqual(pin.With, []string{"playwright", "audit-toolkit"}) {
		t.Errorf("WITH parse wrong: %v", pin.With)
	}
	if !reflect.DeepEqual(pin.No, []string{"aider"}) {
		t.Errorf("NO parse wrong: %v", pin.No)
	}
	if !reflect.DeepEqual(pin.Extensions, []string{"claude-mem", "context7"}) {
		t.Errorf("CATALOG parse wrong: %v", pin.Extensions)
	}
	if len(notes) != len(kvs) {
		t.Errorf("expected one note per input key, got %d / %d", len(notes), len(kvs))
	}
}

